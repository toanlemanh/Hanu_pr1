package tut02;

public class Act02 {
    public static void main(String[] args) {
        System.out.println(" Find the max number of three numbers ");
        int a = 3;
        int b = 6;
        int c = 2;
        int max = 0;
        if (max <= a){
            max = a;
        }
        if (max <= b){
            max = b;
        }
        if (max <= c){
            max = c;
        }
        System.out.printf("Max = %d", max);
    }
}
